CREATE TABLE SECOND_TITLE(SECOND_TITLE_ID NUMERIC(8,0) NOT NULL PRIMARY KEY, SECOND_TITLE_NAME VARCHAR(20), FIRST_TITLE_ID NUMERIC(8,0));
CREATE TABLE THIRD_TITLE(THIRD_TITLE_ID NUMERIC(8,0) NOT NULL PRIMARY KEY, THIRD_TITLE_NAME VARCHAR(20), SECOND_TITLE_ID NUMERIC(8,0));

